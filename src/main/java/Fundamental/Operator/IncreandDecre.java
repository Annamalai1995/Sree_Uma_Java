package Fundamental.Operator;

public class IncreandDecre {
    public static void main(String[] args) {
        //pre increment;
        int sree=1001;
        int uma=999;
        System.out.println(++sree);
        System.out.println(--uma);
        //post incremnet
        System.out.println(sree++);
        System.out.println(uma--);

    }
}
